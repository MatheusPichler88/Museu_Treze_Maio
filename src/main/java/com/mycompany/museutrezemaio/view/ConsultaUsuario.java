     private void configurarTabela() {
        // Criar modelo com colunas
        String[] colunas = {"ID", "Nome", "Tipo", "Telefone", "Email"};
        modelo = new DefaultTableModel(colunas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        tabelaUsuarios.setModel(modelo);
    }
     
     private void carregarUsuarios() {
         try {
        modelo.setRowCount(0);
        UsuarioController controller = new UsuarioController();
        ArrayList<Usuario> lista = controller.listarUsuarios();
        
        for (Usuario u : lista) {
            Object[] linha = { u.getIdUsuario(), u.getNome(), u.getTipo(), u.getTelefone(), u.getEmail() };
            modelo.addRow(linha);
        }
        
        lblTotalUsuarios.setText("Total: " + lista.size() + " usuários");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }
     }

