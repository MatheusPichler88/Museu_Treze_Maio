    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String texto = txtBuscar.getText().trim();
        
        if (texto.isEmpty()) {
            carregarUsuarios();
            return;
        }
        
        try {
            modelo.setRowCount(0);
            
            UsuarioDAO dao = new UsuarioDAO();
            ArrayList<Usuario> lista = dao.getUsuarios();
            
            int encontrados = 0;
            
            // Filtrar por nome
            for (Usuario u : lista) {
                if (u.getNome().toLowerCase().contains(texto.toLowerCase())) {
                    modelo.addRow(new Object[]{
                        u.getIdUsuario(),
                        u.getNome(),
                        u.getTipo(),
                        u.getTelefone(),
                        u.getEmail()
                    });
                    encontrados++;
                }
            }
            
            lblTotalUsuarios.setText("Encontrados: " + encontrados + " usuários");
            
            if (encontrados == 0) {
                JOptionPane.showMessageDialog(this, "Nenhum usuário encontrado!");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro na busca: " + e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

