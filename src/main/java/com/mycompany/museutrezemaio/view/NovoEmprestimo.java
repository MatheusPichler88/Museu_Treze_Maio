    private void bntBuscarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntBuscarUsuarioActionPerformed
    //Pega o ID digitado
    String idTexto = txtIdUsuario.getText().trim();
    // Verifica se digitou algo
    if (idTexto.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Digite um ID de usuário!");
        return;
    }
    try {
        // Converte para número
        int id = Integer.parseInt(idTexto);
        
        // Buscar no banco
        UsuarioDAO dao = new UsuarioDAO();
        com.mycompany.museutrezemaio.model.Usuario usuario = dao.getUsuario(id);
        
        // Verifica caso tenha achado
        if (usuario == null) {
            JOptionPane.showMessageDialog(this, "Usuário não encontrado!");
            lblNomeUsuario.setText("Nome:");
            lblTipoUsario.setText("Tipo:");
        } else {
            // Mostrar os dados
            lblNomeUsuario.setText("Nome: " + usuario.getNome());
            lblTipoUsario.setText("Tipo: " + usuario.getTipo());
        }
        
    } catch (NumberFormatException e) {
        // Erro se não for número
        JOptionPane.showMessageDialog(this, "Digite apenas números no ID!");
    } catch (Exception e) {
        // Outros erros
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }

                                             
    }//GEN-LAST:event_bntBuscarUsuarioActionPerformed

