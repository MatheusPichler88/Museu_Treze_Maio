    private void bntRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntRegistrarActionPerformed
       try {
        // Pega dados da tela
        String idUsuarioTexto = txtIdUsuario.getText().trim();
        String idMaterialTexto = txtIdMaterial.getText().trim();
        String dataTexto = txtDataPrevista.getText().trim();
        
        // Verifica se preencheu tudo
        if (idUsuarioTexto.isEmpty() || idMaterialTexto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha ID do usuário e material!");
            return;
        }
        
        // Converte para números
        int idUsuario = Integer.parseInt(idUsuarioTexto);
        int idMaterial = Integer.parseInt(idMaterialTexto);
        
        // Converte data
        java.sql.Date dataDevolucao;
        try {
            // Formato: dd/mm/aaaa para aaaa-mm-dd
            String[] partes = dataTexto.split("/");
            String dataSQL = partes[2] + "-" + partes[1] + "-" + partes[0];
            dataDevolucao = java.sql.Date.valueOf(dataSQL);
        } catch (Exception e) {
            // Se data errada, usa 15 dias a partir de hoje
            dataDevolucao = new java.sql.Date(System.currentTimeMillis() + (15L * 24 * 60 * 60 * 1000));
        }
        
        // Verifica se usuário existe
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        com.mycompany.museutrezemaio.model.Usuario usuario = usuarioDAO.getUsuario(idUsuario);
        if (usuario == null) {
            JOptionPane.showMessageDialog(this, "Usuário não existe!");
            return;
        }
        
        // Verifica se material existe e está disponível
        MaterialBibliotecaDAO materialDAO = new MaterialBibliotecaDAO();
        com.mycompany.museutrezemaio.model.MaterialBiblioteca material = materialDAO.getMaterial(idMaterial);
        if (material == null) {
            JOptionPane.showMessageDialog(this, "Material não existe!");
            return;
        }
        if (!material.isDisponivel()) {
            JOptionPane.showMessageDialog(this, "Material não está disponível!");
            return;
        }
        
        // Cria o empréstimo
        com.mycompany.museutrezemaio.model.Emprestimo emprestimo = new com.mycompany.museutrezemaio.model.Emprestimo();
        emprestimo.setIdMaterial(idMaterial);
        emprestimo.setIdUsuario(idUsuario);
        emprestimo.setDataDevolucaoPrevista(dataDevolucao);
        emprestimo.setStatusEmprestimo("Ativo");
        
        // Salva no banco
        EmprestimoDAO emprestimoDAO = new EmprestimoDAO();
        emprestimoDAO.inserir(emprestimo);
        
        // Marca material como emprestado
        materialDAO.atualizarDisponibilidade(idMaterial, false);
    
        JOptionPane.showMessageDialog(this,
            "Empréstimo registrado!\n" +
            "Usuário: " + usuario.getNome() + "\n" +
            "Livro: " + material.getTitulo() + "\n" +
            "Devolver até: " + dataDevolucao);

        txtIdUsuario.setText("");
        txtIdMaterial.setText("");
        lblNomeUsuario.setText("Nome:");
        lblTipoUsario.setText("Tipo:");
        lblTituloMaterial.setText("Título:");
        lblDisponivel.setText("Disponível:");
        txtObs.setText("");
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "IDs devem ser números!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }
    
    }//GEN-LAST:event_bntRegistrarActionPerformed

