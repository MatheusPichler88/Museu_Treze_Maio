    private void bntBuscarMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntBuscarMaterialActionPerformed
    try {
        // Pega ID
        String idTexto = txtIdMaterial.getText().trim();
        int id = Integer.parseInt(idTexto);
        
        // 2. Busca material
        MaterialBibliotecaDAO dao = new MaterialBibliotecaDAO();
        com.mycompany.museutrezemaio.model.MaterialBiblioteca material = dao.getMaterial(id);
        
        // 3. Mostra resultados
        if (material != null) {
            lblTituloMaterial.setText("Título: " + material.getTitulo());
            
            if (material.isDisponivel()) {
                lblDisponivel.setText("Disponível: Sim");
            } else {
                lblDisponivel.setText("Disponível: Não");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Material não existe!");
        }
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro! Digite um número válido.");
    }

    }//GEN-LAST:event_bntBuscarMaterialActionPerformed

