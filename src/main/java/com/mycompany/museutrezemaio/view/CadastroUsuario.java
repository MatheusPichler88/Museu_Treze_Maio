    private void bntSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSalvarActionPerformed
             try {
            //Pega os dados dos campos
            String nome = txtNome.getText().trim();
            String tipo = cmbTipo.getSelectedItem().toString();
            String telefone = txtTelefone.getText().trim();
            String email = txtEmail.getText().trim();
            String endereco = txtEndereco.getText().trim();

            // Verifica se campos estão preenchidos
            if (nome.isEmpty() || telefone.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                    "Os campos NOME e TELEFONE são obrigatórios!",
                    "Atenção",
                    JOptionPane.WARNING_MESSAGE);
                return;
            }

            UsuarioController usuarioController = new UsuarioController();
boolean sucesso = usuarioController.cadastrarUsuario(nome, tipo, telefone, email, endereco);

if (sucesso) {
    JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso!");
    limparCampos();
} else {
    JOptionPane.showMessageDialog(this, "Erro ao cadastrar usuário!");
}

            JOptionPane.showMessageDialog(this,
                "Usuário cadastrado com sucesso!",
                "Sucesso",
                JOptionPane.INFORMATION_MESSAGE);

            limparCampos();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Erro ao cadastrar usuário:\n" + e.getMessage(),
                "Erro",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    

    }//GEN-LAST:event_bntSalvarActionPerformed

