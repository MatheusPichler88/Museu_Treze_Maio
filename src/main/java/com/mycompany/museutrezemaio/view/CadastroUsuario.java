     private void limparCampos() {
        txtNome.setText("");
        cmbTipo.setSelectedIndex(0); // Volta para "Comunidade"
        txtTelefone.setText("");
        txtEmail.setText("");
        txtEndereco.setText("");
        txtNome.requestFocus();
    }
